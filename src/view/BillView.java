    private void txtPrice1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPrice1FocusLost
        if(txtPrice1.getText().equals("")|| txtPrice2.getText().equals(""))
        {
            return;
        }
        float Price1= Float.parseFloat(txtPrice1.getText());
        float Price2= Float.parseFloat(txtPrice2.getText());
        
        
        float PriceTotal= Price1+Price2;
        String st=String.format("%.2f", PriceTotal);
        txtTotalPrice.setText(st);
        
    }//GEN-LAST:event_txtPrice1FocusLost
