     private void LoadDatatoTable()
    {
       try {
            BillDao dao= new BillDao();
           // List<BillModel> list=dao.FindByAll();
           List<BillModel> list= dao.FindByAll();
            tblModel.setRowCount(0);
            for(BillModel it:list)
            {
                tblModel.addRow(new Object[]{it.getIDBill(),
                it.getIDStdudent(),it.getFullName(),it.getIDRooms(),it.getMonth(),it.getPrice1(),it.getCategory(),it.getPrice2(),it.getTotalPrice(),it.getDate()
                
            });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentFrom, e.getMessage(), "Lỗi");
        }
    }
    private void loadCMB()
    {
//        int i=0;
        try {
            Connection con= DatabaseHelper.openConnection();
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery(" select IDRooms from Rooms ");
            while(rs.next())
            {
//                i=1;
                cmbRoom.addItem(rs.getString("IDRooms"));
            }
            rs.close();
            con.close();
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
